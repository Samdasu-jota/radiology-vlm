import json
from PIL import Image
from torch.utils.data import Dataset
from transformers import CLIPProcessor


class NegationAwareDataset(Dataset):
    """Dataset that returns (image, positive text, hard negative text) triples.

    The hard negative is generated by flipping present↔absent labels,
    forcing the model to learn negation semantics.
    """

    def __init__(self, json_path, processor):
        self.processor = processor

        with open(json_path, "r") as f:
            all_pairs = json.load(f)

        # Only keep pairs that have at least one present or absent finding
        self.pairs = [
            p for p in all_pairs
            if p["labels"]["present"] or p["labels"]["absent"]
        ]
        print(f"NegationAwareDataset: {len(self.pairs)} pairs "
              f"(filtered from {len(all_pairs)}, dropped {len(all_pairs) - len(self.pairs)} with no findings)")

    def __len__(self):
        return len(self.pairs)

    @staticmethod
    def flip_labels(labels):
        """Swap present↔absent to create a hard negative text."""
        flipped_present = labels["absent"]
        flipped_absent = labels["present"]
        parts = []
        if flipped_present:
            parts.append("present: " + ", ".join(flipped_present))
        if flipped_absent:
            parts.append("absent: " + ", ".join(flipped_absent))
        return ". ".join(parts) + "." if parts else "no findings mentioned."

    def _tokenize(self, text):
        tokens = self.processor.tokenizer(
            text,
            return_tensors="pt",
            padding="max_length",
            max_length=77,
            truncation=True,
        )
        return tokens["input_ids"].squeeze(0), tokens["attention_mask"].squeeze(0)

    def __getitem__(self, idx):
        pair = self.pairs[idx]

        image = Image.open(pair["image_path"]).convert("RGB")
        inputs = self.processor(images=image, return_tensors="pt")
        pixel_values = inputs["pixel_values"].squeeze(0)

        # Positive text (structured labels)
        pos_ids, pos_mask = self._tokenize(pair["structured_text"])

        # Hard negative text (flipped labels)
        neg_text = self.flip_labels(pair["labels"])
        neg_ids, neg_mask = self._tokenize(neg_text)

        return {
            "pixel_values": pixel_values,
            "input_ids": pos_ids,
            "attention_mask": pos_mask,
            "neg_input_ids": neg_ids,
            "neg_attention_mask": neg_mask,
        }


